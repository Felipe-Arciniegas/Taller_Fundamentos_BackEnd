#Concatenación de Listas : Crea dos listas con diferentes elementos, luego combínalas en una sola lista y ordénalos.
print("\t Concatenación de Listas.")
lista1 = [1, 2, 3, 4, 5]
lista2 = [6, 7, 8, 9, 10]
lista3 = lista1 + lista2
lista3.sort()
print(f"Lista 1: {lista1}")
print(f"Lista 2: {lista2}")
print(f"Lista 3: {lista3}")